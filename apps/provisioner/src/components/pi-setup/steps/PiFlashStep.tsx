export function PiFlashStep({ imagePath, piConfig, onNext, onBack }: PiFlashStepProps) {
  if (isTauri()) {
    return <FlashWizard imagePath={imagePath} piConfig={piConfig} onNext={onNext} onBack={onBack} />;
  }
  return <ManualChecklist onNext={onNext} onBack={onBack} />;
}

