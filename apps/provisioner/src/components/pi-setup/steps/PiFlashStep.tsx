// WRITE BOOT FILES
// ============================================

async function writeBootFiles(
  bootLetter: string,
  cfg: PiFlashConfig,
  onStatus: (msg: string) => void,
): Promise<void> {
  const base = `${bootLetter}:`;

  onStatus('Hashing password…');
  const hashedPassword = await invoke<string>('hash_password', { password: cfg.password });

  onStatus('Writing ssh…');
  await invoke('write_file', { path: `${base}\\ssh`, content: '' });

  onStatus('Writing userconf.txt…');
  await invoke('write_file', {
    path: `${base}\\userconf.txt`,
    content: buildUserconf(cfg.username, hashedPassword),
  });

  if (cfg.wifiSsid && cfg.wifiPassword) {
    onStatus('Writing wpa_supplicant.conf…');
    await invoke('write_file', {
      path: `${base}\\wpa_supplicant.conf`,
      content: buildWpaSupplicant(cfg.wifiSsid, cfg.wifiPassword, cfg.wifiCountry),
    });
  }

  onStatus('Writing custom.toml…');
  await invoke('write_file', { path: `${base}\\custom.toml`, content: buildCustomToml(cfg) });

  onStatus('Writing firstrun.sh…');
  await invoke('write_file', { path: `${base}\\firstrun.sh`, content: buildFirstRun(cfg) });

  onStatus('Writing freemen-install.sh…');
  await invoke('write_file', { path: `${base}\\freemen-install.sh`, content: buildInstallScript(cfg) });

  // Patch cmdline.txt — append systemd.run directive so firstrun.sh auto-executes
  onStatus('Patching cmdline.txt…');
  try {
    const existing = await invoke<string>('read_file', { path: `${base}\\cmdline.txt` });
    const line = existing.trimEnd();
    if (!line.includes('firstrun.sh')) {
      // Raspberry Pi OS Bookworm mounts the boot partition at /boot/firmware.
      // Older releases (Bullseye) used /boot. We always write /boot/firmware
      // because the downloaded image is the current Bookworm release.
      const patched =
        line +
        ' systemd.run=/boot/firmware/firstrun.sh systemd.run_success_action=reboot systemd.unit=kernel-command-line.target\n';
      await invoke('write_file', { path: `${base}\\cmdline.txt`, content: patched });
    }
  } catch {
    // cmdline.txt patch is best-effort; if it fails the user can run firstrun.sh manually
    console.warn('Could not patch cmdline.txt — firstrun.sh will not auto-run on first boot');
  }
}

// Detect the boot partition drive letter by polling detect_drives
async function detectBootPartition(maxWaitMs = 30_000): Promise<string | null> {
  const deadline = Date.now() + maxWaitMs;
  while (Date.now() < deadline) {
    try {
      const drives = await invoke<Array<{
        letter: string;
        label: string;
        size: number;
        is_removable: boolean;
      }>>('detect_drives');

      const boot = drives.find(
        (d) =>
          d.label.toLowerCase().includes('boot') ||
          (d.is_removable && d.size > 0 && d.size < 800 * 1024 * 1024),
      );
      if (boot) return boot.letter;
    } catch {
      // ignore transient errors while disk remounts
    }
    await new Promise((r) => setTimeout(r, 3000));
  }
  return null;
}

