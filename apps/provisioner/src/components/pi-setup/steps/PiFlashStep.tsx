// HELPERS — boot file generation
// ============================================

function buildUserconf(username: string, hashedPassword: string): string {
  return `${username}:${hashedPassword}`;
}

function buildWpaSupplicant(ssid: string, password: string, country: string): string {
  return `country=${country}
ctrl_interface=DIR=/var/run/wpa_supplicant GROUP=netdev
update_config=1

network={
    ssid="${ssid}"
    psk="${password}"
    key_mgmt=WPA-PSK
}
`;
}

function buildCustomToml(cfg: PiFlashConfig): string {
  const wifi = cfg.wifiSsid
    ? `\n[wlan]\nssid = "${cfg.wifiSsid}"\npassword = "${cfg.wifiPassword}"\ncountry = "${cfg.wifiCountry}"\n`
    : '';
  return `# Raspberry Pi OS Configuration — generated by Freemen Provisioner

[system]
hostname = "${cfg.hostname}"

[user]
name = "${cfg.username}"

[ssh]
enabled = true
${wifi}`;
}

/**
 * firstrun.sh — runs via systemd.run at kernel-command-line.target (NO network yet).
 * Only does offline work: hostname, SSH, WiFi config, then installs the
 * freemen-install.service that runs AFTER network-online.target.
 */
function buildFirstRun(cfg: PiFlashConfig): string {
  return `#!/bin/bash
# Freemen first-boot stage 1 — offline setup only.
# Network-dependent installation happens in freemen-install.service on next boot.
set +e

BOOT_MOUNT="/boot/firmware"
[ -d "$BOOT_MOUNT" ] || BOOT_MOUNT="/boot"
BOOT_LOG="$BOOT_MOUNT/freemen-setup.log"

log() { echo "[$(date '+%Y-%m-%d %H:%M:%S')] $*" | tee -a "$BOOT_LOG"; }
set_status() { echo "$1" > "$BOOT_MOUNT/freemen-status.txt" 2>/dev/null || true; log "STATUS: $1"; }

set_status "FIRSTRUN_STARTED"
log "=== firstrun.sh started (stage 1 — offline) ==="

# 1. Hostname
CURRENT_HOSTNAME=$(cat /etc/hostname | tr -d " \\t\\n\\r")
if raspi-config nonint do_hostname "${cfg.hostname}" 2>/dev/null; then
    log "Hostname set to ${cfg.hostname}"
else
    echo "${cfg.hostname}" > /etc/hostname
    sed -i "s/127.0.1.1.*$CURRENT_HOSTNAME/127.0.1.1\\t${cfg.hostname}/g" /etc/hosts
    log "Hostname set (fallback method)"
fi

# 2. SSH
systemctl enable ssh 2>/dev/null && log "SSH enabled" || log "WARNING: ssh enable failed"

# 3. Copy freemen-install.sh from boot partition to /usr/local/bin
if [ -f "$BOOT_MOUNT/freemen-install.sh" ]; then
    cp "$BOOT_MOUNT/freemen-install.sh" /usr/local/bin/freemen-install.sh
    chmod +x /usr/local/bin/freemen-install.sh
    log "freemen-install.sh copied to /usr/local/bin"
else
    set_status "ERROR_INSTALL_SCRIPT_MISSING"
    log "ERROR: freemen-install.sh not found on boot partition"
    exit 1
fi

# 4. Create systemd service to run installation after network is online
cat > /etc/systemd/system/freemen-install.service << 'SERVICE_EOF'
[Unit]
Description=Freemen Printer Proxy Installation
After=network-online.target
Wants=network-online.target
ConditionPathExists=/usr/local/bin/freemen-install.sh

[Service]
Type=oneshot
ExecStart=/usr/local/bin/freemen-install.sh
RemainAfterExit=yes
StandardOutput=journal+console
StandardError=journal+console

[Install]
WantedBy=multi-user.target
SERVICE_EOF

systemctl enable freemen-install.service && log "freemen-install.service enabled" || log "WARNING: service enable failed"

# 5. Clean up cmdline.txt so firstrun.sh doesn't run again
sed -i 's| systemd.run=[^ ]*firstrun.sh[^ ]*||g' "$BOOT_MOUNT/cmdline.txt" 2>/dev/null || true
rm -f "$BOOT_MOUNT/freemen-install.sh"

set_status "FIRSTRUN_COMPLETE_REBOOTING"
log "=== stage 1 complete — rebooting to run installation ==="
reboot
`;
}

/**
 * freemen-install.sh — written to boot partition, copied by firstrun.sh.
 * Runs as a systemd service AFTER network-online.target on second boot.
 * All network-dependent work (Docker, cloudflared, proxy) happens here.
 */
function buildInstallScript(cfg: PiFlashConfig): string {
  return `#!/bin/bash
# Freemen first-boot stage 2 — network installation.
# Runs as freemen-install.service after network-online.target.
set +e

BOOT_MOUNT="/boot/firmware"
[ -d "$BOOT_MOUNT" ] || BOOT_MOUNT="/boot"
BOOT_LOG="$BOOT_MOUNT/freemen-setup.log"
SYS_LOG="/var/log/freemen-setup.log"

log() { echo "[$(date '+%Y-%m-%d %H:%M:%S')] $*" | tee -a "$SYS_LOG" "$BOOT_LOG"; }
set_status() { echo "$1" > "$BOOT_MOUNT/freemen-status.txt" 2>/dev/null || true; log "STATUS: $1"; }

set_status "INSTALL_STARTED"
log "=== freemen-install.sh started (stage 2 — network) ==="

# 1. Confirm network connectivity
set_status "CHECKING_NETWORK"
NETWORK_UP=0
for i in $(seq 1 30); do
    if ping -c 1 -W 3 1.1.1.1 >/dev/null 2>&1; then
        log "Network confirmed after \${i}x3s"
        NETWORK_UP=1
        break
    fi
    sleep 3
done
if [ "\$NETWORK_UP" -eq 0 ]; then
    set_status "ERROR_NO_NETWORK"
    log "ERROR: No network. Check ethernet cable or WiFi credentials."
    exit 1
fi

# 2. System updates
set_status "UPDATING_SYSTEM"
apt-get update -q && log "apt-get update done" || { set_status "ERROR_APT_UPDATE"; log "ERROR: apt-get update failed"; exit 1; }
apt-get install -y -q curl ca-certificates && log "packages installed" || { set_status "ERROR_APT_INSTALL"; log "ERROR: apt install failed"; exit 1; }

# 3. Docker
set_status "INSTALLING_DOCKER"
if ! command -v docker >/dev/null 2>&1; then
    log "Installing Docker..."
    curl -fsSL https://get.docker.com | sh && log "Docker installed" || { set_status "ERROR_DOCKER_INSTALL"; log "ERROR: Docker install failed"; exit 1; }
    usermod -aG docker "${cfg.username}"
    systemctl enable docker
    systemctl start docker
else
    log "Docker already present"
fi

# 4. cloudflared
set_status "INSTALLING_CLOUDFLARED"
if ! command -v cloudflared >/dev/null 2>&1; then
    log "Downloading cloudflared..."
    curl -fsSL -o /usr/local/bin/cloudflared \\
        https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-arm64 \\
        && chmod +x /usr/local/bin/cloudflared \\
        && log "cloudflared downloaded" \\
        || { set_status "ERROR_CLOUDFLARED_DOWNLOAD"; log "ERROR: cloudflared download failed"; exit 1; }
fi

set_status "CONFIGURING_TUNNEL"
cloudflared service install "${cfg.tunnelToken}" && log "Tunnel service installed" || { set_status "ERROR_TUNNEL_INSTALL"; log "ERROR: tunnel token rejected or cloudflared install failed"; exit 1; }
systemctl enable cloudflared
systemctl start cloudflared && log "cloudflared started" || log "WARNING: cloudflared start failed"

# 5. Freemen Printer Proxy
set_status "INSTALLING_PROXY"
mkdir -p /opt/freemen-printer-proxy
cat > /opt/freemen-printer-proxy/docker-compose.yml << 'COMPOSE_EOF'
version: '3'
services:
  printer-proxy:
    image: ghcr.io/potarius/freemen-printer-proxy:latest
    restart: unless-stopped
    ports:
      - "${cfg.printerPort}:6500"
    environment:
      - NODE_ENV=production
      - API_KEY=${cfg.apiKey || 'change-me'}
COMPOSE_EOF

cd /opt/freemen-printer-proxy
docker compose pull && log "Image pulled" || { set_status "ERROR_DOCKER_PULL"; log "ERROR: docker pull failed"; exit 1; }
docker compose up -d && log "Proxy container started" || { set_status "ERROR_DOCKER_UP"; log "ERROR: docker compose up failed"; exit 1; }

cat > /etc/systemd/system/freemen-printer-proxy.service << 'SVC_EOF'
[Unit]
Description=Freemen Printer Proxy
After=docker.service network-online.target
Requires=docker.service

[Service]
WorkingDirectory=/opt/freemen-printer-proxy
ExecStart=/usr/bin/docker compose up
ExecStop=/usr/bin/docker compose down
Restart=always

[Install]
WantedBy=multi-user.target
SVC_EOF
systemctl enable freemen-printer-proxy

# 6. Self-disable so this service doesn't run again
systemctl disable freemen-install.service 2>/dev/null || true
rm -f /usr/local/bin/freemen-install.sh /etc/systemd/system/freemen-install.service

set_status "COMPLETE"
log "=== Freemen installation complete ==="
`;
}

