/**
 * Pi Setup - Flash SD Card Step
 *
 * Receives the already-downloaded image path and full Pi + tunnel config.
 * Flow: disk-select → confirm → preparing → flashing → writing-files → done
 *
 * After writing the OS image, this step:
 *  1. Restores the disk (lets Windows remount partitions)
 *  2. Detects the boot partition
 *  3. Hashes the password via the Tauri hash_password command
 *  4. Writes ssh, userconf.txt, wpa_supplicant.conf, custom.toml, firstrun.sh
 *  5. Patches cmdline.txt to auto-run firstrun.sh on first boot
 *
 * Browser fallback: manual checklist.
 */

