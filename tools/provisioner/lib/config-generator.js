/**
 * Device Configuration Generator
 * Generates local config files for device deployment
 */

const fs = require('fs');
const path = require('path');
const { v4: uuidv4 } = require('uuid');

class ConfigGenerator {
  constructor(outputDir) {
    this.outputDir = outputDir;
    this.ensureDir(outputDir);
  }

  ensureDir(dir) {
    if (!fs.existsSync(dir)) {
      fs.mkdirSync(dir, { recursive: true });
    }
  }

  /**
   * Generate a unique device ID
   */
  generateDeviceId() {
    return `freemen-${uuidv4().split('-')[0]}`;
  }

  /**
   * Generate device configuration
   */
  generateDeviceConfig(options) {
    const {
      deviceId,
      deviceName,
      targetPlatform,
      hostname,
      tunnelName,
      tunnelId,
      tunnelToken,
      zoneId,
      zoneName,
      accountId,
      servicePort = 6500,
    } = options;

    const config = {
      device: {
        id: deviceId,
        name: deviceName,
        platform: targetPlatform,
        createdAt: new Date().toISOString(),
      },
      cloudflare: {
        accountId,
        zoneId,
        zoneName,
        tunnel: {
          id: tunnelId,
          name: tunnelName,
          hostname,
        },
      },
      service: {
        port: servicePort,
        localUrl: `http://localhost:${servicePort}`,
        publicUrl: `https://${hostname}`,
      },
    };

    return config;
  }

  /**
   * Generate .env file content for the device
   */
  generateEnvContent(options) {
    const {
      tunnelToken,
      servicePort = 6500,
      hostname,
      deviceId,
    } = options;

    return `# Freemen Printer Proxy - Device Configuration
# Generated by Freemen Provisioner
# DO NOT COMMIT THIS FILE

# Device Identity
DEVICE_ID=${deviceId}

# Service Configuration
PORT=${servicePort}
API_KEY=\${API_KEY:-change-me-in-production}

# Cloudflare Tunnel
TUNNEL_TOKEN=${tunnelToken}
PUBLIC_HOSTNAME=${hostname}

# Printer (configure after deployment)
PRINTER_IP=
PRINTER_PORT=9100
PRINTER_PROTOCOL=jetdirect
`;
  }

  /**
   * Generate docker-compose override for cloudflared
   */
  generateDockerComposeOverride(options) {
    const { tunnelToken, servicePort = 6500 } = options;

    return `# Freemen Printer Proxy - Docker Compose Override
# Generated by Freemen Provisioner
# DO NOT COMMIT THIS FILE

version: '3.8'

services:
  freemen-printer-proxy:
    environment:
      - TUNNEL_TOKEN=${tunnelToken}

  cloudflared:
    image: cloudflare/cloudflared:latest
    container_name: freemen-cloudflared
    restart: unless-stopped
    command: tunnel --no-autoupdate run --token ${tunnelToken}
    networks:
      - freemen-printer-proxy
    depends_on:
      - freemen-printer-proxy

networks:
  freemen-printer-proxy:
    external: true
`;
  }

  /**
   * Generate cloudflared systemd service (for non-Docker installs)
   */
  generateSystemdService(options) {
    const { tunnelToken } = options;

    return `[Unit]
Description=Cloudflare Tunnel for Freemen Printer Proxy
After=network.target

[Service]
Type=simple
User=root
ExecStart=/usr/local/bin/cloudflared tunnel --no-autoupdate run --token ${tunnelToken}
Restart=on-failure
RestartSec=5

[Install]
WantedBy=multi-user.target
`;
  }

  /**
   * Generate setup script for Raspberry Pi
   */
  generatePiSetupScript(options) {
    const { tunnelToken, hostname, deviceId } = options;

    return `#!/bin/bash
# Freemen Printer Proxy - Raspberry Pi Setup Script
# Generated by Freemen Provisioner

set -e

echo "╔══════════════════════════════════════════════════════════╗"
echo "║     Freemen Printer Proxy - Raspberry Pi Setup           ║"
echo "║     Device: ${deviceId}                              ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""

# Check if running as root
if [ "$EUID" -ne 0 ]; then
  echo "Please run as root (sudo ./setup-pi.sh)"
  exit 1
fi

# Install Docker if not present
if ! command -v docker &> /dev/null; then
  echo "Installing Docker..."
  curl -fsSL https://get.docker.com | sh
  usermod -aG docker pi
fi

# Install cloudflared
if ! command -v cloudflared &> /dev/null; then
  echo "Installing cloudflared..."
  curl -L https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-arm64 -o /usr/local/bin/cloudflared
  chmod +x /usr/local/bin/cloudflared
fi

# Copy configuration files
echo "Setting up configuration..."
cp device.env /opt/freemen-printer-proxy/.env
cp docker-compose.cloudflare.yml /opt/freemen-printer-proxy/docker-compose.override.yml

# Install systemd service for cloudflared
cp cloudflared.service /etc/systemd/system/freemen-cloudflared.service
systemctl daemon-reload
systemctl enable freemen-cloudflared

# Start services
cd /opt/freemen-printer-proxy
docker compose up -d
systemctl start freemen-cloudflared

echo ""
echo "╔══════════════════════════════════════════════════════════╗"
echo "║                    Setup Complete!                       ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""
echo "  Local:  http://localhost:6500"
echo "  Public: https://${hostname}"
echo ""
echo "  Commands:"
echo "  - View logs:     docker compose logs -f"
echo "  - Tunnel status: systemctl status freemen-cloudflared"
echo ""
`;
  }

  /**
   * Generate setup script for Ubuntu/Linux
   */
  generateLinuxSetupScript(options) {
    const { tunnelToken, hostname, deviceId } = options;

    return `#!/bin/bash
# Freemen Printer Proxy - Linux Setup Script
# Generated by Freemen Provisioner

set -e

echo "╔══════════════════════════════════════════════════════════╗"
echo "║     Freemen Printer Proxy - Linux Setup                  ║"
echo "║     Device: ${deviceId}                              ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""

# Check if running as root
if [ "$EUID" -ne 0 ]; then
  echo "Please run as root (sudo ./setup-linux.sh)"
  exit 1
fi

# Detect architecture
ARCH=$(uname -m)
case $ARCH in
  x86_64) CF_ARCH="amd64" ;;
  aarch64) CF_ARCH="arm64" ;;
  *) echo "Unsupported architecture: $ARCH"; exit 1 ;;
esac

# Install Docker if not present
if ! command -v docker &> /dev/null; then
  echo "Installing Docker..."
  curl -fsSL https://get.docker.com | sh
fi

# Install cloudflared
if ! command -v cloudflared &> /dev/null; then
  echo "Installing cloudflared..."
  curl -L "https://github.com/cloudflare/cloudflared/releases/latest/download/cloudflared-linux-\${CF_ARCH}" -o /usr/local/bin/cloudflared
  chmod +x /usr/local/bin/cloudflared
fi

# Create directory
mkdir -p /opt/freemen-printer-proxy
cd /opt/freemen-printer-proxy

# Clone or update repository
if [ -d ".git" ]; then
  git pull
else
  git clone https://github.com/freemen-solutions/freemen-printer-proxy.git .
fi

# Copy configuration files
cp /path/to/provisioned/device.env .env
cp /path/to/provisioned/docker-compose.cloudflare.yml docker-compose.override.yml

# Install systemd service for cloudflared
cat > /etc/systemd/system/freemen-cloudflared.service << 'EOF'
[Unit]
Description=Cloudflare Tunnel for Freemen Printer Proxy
After=network.target docker.service

[Service]
Type=simple
ExecStart=/usr/local/bin/cloudflared tunnel --no-autoupdate run --token ${tunnelToken}
Restart=on-failure
RestartSec=5

[Install]
WantedBy=multi-user.target
EOF

systemctl daemon-reload
systemctl enable freemen-cloudflared

# Start services
docker compose up -d
systemctl start freemen-cloudflared

echo ""
echo "╔══════════════════════════════════════════════════════════╗"
echo "║                    Setup Complete!                       ║"
echo "╚══════════════════════════════════════════════════════════╝"
echo ""
echo "  Local:  http://localhost:6500"
echo "  Public: https://${hostname}"
echo ""
`;
  }

  /**
   * Write all config files to output directory
   */
  writeAllConfigs(options) {
    const files = [];

    // Device config JSON
    const deviceConfig = this.generateDeviceConfig(options);
    const deviceConfigPath = path.join(this.outputDir, 'device.json');
    fs.writeFileSync(deviceConfigPath, JSON.stringify(deviceConfig, null, 2));
    files.push({ name: 'device.json', path: deviceConfigPath, description: 'Device configuration' });

    // Environment file
    const envContent = this.generateEnvContent(options);
    const envPath = path.join(this.outputDir, 'device.env');
    fs.writeFileSync(envPath, envContent);
    files.push({ name: 'device.env', path: envPath, description: 'Environment variables' });

    // Docker compose override
    const composeOverride = this.generateDockerComposeOverride(options);
    const composePath = path.join(this.outputDir, 'docker-compose.cloudflare.yml');
    fs.writeFileSync(composePath, composeOverride);
    files.push({ name: 'docker-compose.cloudflare.yml', path: composePath, description: 'Docker Compose with cloudflared' });

    // Systemd service
    const systemdService = this.generateSystemdService(options);
    const systemdPath = path.join(this.outputDir, 'cloudflared.service');
    fs.writeFileSync(systemdPath, systemdService);
    files.push({ name: 'cloudflared.service', path: systemdPath, description: 'Systemd service for cloudflared' });

    // Platform-specific setup script
    if (options.targetPlatform === 'raspberry-pi') {
      const piScript = this.generatePiSetupScript(options);
      const piScriptPath = path.join(this.outputDir, 'setup-pi.sh');
      fs.writeFileSync(piScriptPath, piScript, { mode: 0o755 });
      files.push({ name: 'setup-pi.sh', path: piScriptPath, description: 'Raspberry Pi setup script' });
    } else {
      const linuxScript = this.generateLinuxSetupScript(options);
      const linuxScriptPath = path.join(this.outputDir, 'setup-linux.sh');
      fs.writeFileSync(linuxScriptPath, linuxScript, { mode: 0o755 });
      files.push({ name: 'setup-linux.sh', path: linuxScriptPath, description: 'Linux setup script' });
    }

    // README for the generated package
    const readme = this.generatePackageReadme(options, files);
    const readmePath = path.join(this.outputDir, 'README.md');
    fs.writeFileSync(readmePath, readme);
    files.push({ name: 'README.md', path: readmePath, description: 'Setup instructions' });

    return files;
  }

  /**
   * Generate README for the provisioned package
   */
  generatePackageReadme(options, files) {
    const { deviceId, deviceName, hostname, tunnelName, targetPlatform } = options;

    return `# Freemen Printer Proxy - Device Package

This package was generated by Freemen Provisioner.

## Device Information

| Property | Value |
|----------|-------|
| Device ID | \`${deviceId}\` |
| Device Name | ${deviceName} |
| Platform | ${targetPlatform} |
| Public URL | https://${hostname} |
| Tunnel Name | ${tunnelName} |

## Files Included

${files.map(f => `- **${f.name}** - ${f.description}`).join('\n')}

## Deployment Instructions

### ${targetPlatform === 'raspberry-pi' ? 'Raspberry Pi' : 'Ubuntu/Linux'}

1. Copy this entire folder to your target device
2. Run the setup script:
   \`\`\`bash
   sudo ./${targetPlatform === 'raspberry-pi' ? 'setup-pi.sh' : 'setup-linux.sh'}
   \`\`\`
3. Configure your printer in the dashboard: http://localhost:6500

### Manual Deployment

1. Copy \`device.env\` to \`.env\` in your freemen-printer-proxy directory
2. Copy \`docker-compose.cloudflare.yml\` to \`docker-compose.override.yml\`
3. Start services:
   \`\`\`bash
   docker compose up -d
   \`\`\`
4. Install cloudflared and run with the token in \`device.env\`

## Security Notes

- **DO NOT** commit these files to version control
- The tunnel token is sensitive - treat it like a password
- Rotate tokens if compromised via Cloudflare dashboard

## Support

- Documentation: https://github.com/freemen-solutions/freemen-printer-proxy
- Issues: https://github.com/freemen-solutions/freemen-printer-proxy/issues
`;
  }
}

module.exports = ConfigGenerator;
